-- Fetch only the books that are in stock in the Vancouver store (id: 1)
-- Include only their id, title and ISBN

-- ============ --
-- TEST COMMAND --
-- ============ --
--
-- sqlite3 -column -header supporting-files/test.sqlite3 < answers/04.sql

-- ================ --
-- EXPECTED RESULTS --
-- ================ --
-------------------------------------
-- id          title       isbn
-- ----------  ----------  ----------
-- 1           The Iceman  0345540115
-- 14          Extreme Ow  1250067057
-------------------------------------

-- ============ --
-- SQL SOLUTION --
-- ============ --
--
-- MODIFY THE QUERY BELOW:

SELECT id, title, isbn FROM books
WHERE store_id = 1 AND stock > 0;
